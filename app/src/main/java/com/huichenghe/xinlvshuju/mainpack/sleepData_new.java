public class sleepData_new
{
    private int sleepIcon;
    private String upString;
    private String underString;

    public sleepData_new(int sleepIcon, String upString, String underString) {
        this.sleepIcon = sleepIcon;
        this.upString = upString;
        this.underString = underString;
    }

    public int getSleepIcon() {
        return sleepIcon;
    }

    public String getUpString() {
        return upString;
    }

    public String getUnderString() {
        return underString;
    }
}
